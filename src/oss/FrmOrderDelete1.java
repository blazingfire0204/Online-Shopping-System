        // TODO add your handling code here:
        try{
            String SQL="SELECT * FROM Customers where  C_Name='"+
            jComboBox1.getSelectedItem()+"'";
            ResultSet rs = DB_Part.Query(DB_Part.con, SQL);
            if(rs.next()){
                jTextField1.setText(String.valueOf(rs.getInt(1)));
            }
        }
        catch (SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
        }
        String SQL="Select * from Orders where C_id ='"+jTextField1.getText().trim()+"'";
        try{
            ResultSet rs=DB_Part.Query(DB_Part.con, SQL);
            if(rs.next()){
                jTextField7.setText(rs.getString(2));
                jTextField2.setText(rs.getString(1));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jTextField6.setText(rs.getString(6));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmOrderDelete1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmOrderDelete1.class.getName()).log(Level.SEVERE, null, ex);
        }

