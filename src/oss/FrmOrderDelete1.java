    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jButton1.getText().equals("Search")){
            try {
                jButton1.setText("Cancel Order");
                jTextField1.setEnabled(false);
                String SQL="Select * from Customers where C_id ='"+jTextField1.getText().trim()+"'";
                ResultSet rs = DB_Part.Query(DB_Part.con, "SELECT * FROM Customers");
                while(rs.next()) {
                    jComboBox1.addItem(rs.getString(2));
                }
            }
            catch (SQLException ex) {
                Logger.getLogger(FrmOrderDelete1.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmOrderDelete1.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        else{
            jButton1.setText("Search");
            String SQL="Delete from Orders where O_id ='"+jTextField2.getText().trim()+"'";
            try {
                int ret =DB_Part.UpdateTable(DB_Part.con, SQL);
                if(ret==1){
                    JOptionPane.showMessageDialog(this,"Successful Deleted");
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField5.setText("");
                    jTextField6.setText("");
                    jTextField7.setText("");
                    jComboBox1.removeAllItems();
                    jTextField1.setEnabled(true);
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmOrderDelete1.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FrmOrderDelete1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

